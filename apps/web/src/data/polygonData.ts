// Función para agrupar polígonos por orden y compromiso
export const getPolygonGroups = (): PolygonGroup[] => {
  const groupsMap = new Map<string, PolygonGroup>();

  polygons.forEach((polygon) => {
    const key = polygon.orden;
    if (!groupsMap.has(key)) {
      groupsMap.set(key, {
        orden: polygon.orden,
        compromiso: polygon.compromiso,
        polygons: [],
      });
    }
    groupsMap.get(key)?.polygons.push(polygon);
  });

  return Array.from(groupsMap.values());
};

// Función para obtener compromisos únicos
export const getUniqueCompromisos = (): string[] => {
  return Array.from(new Set(polygons.map((p) => p.compromiso)));
};

// Función para obtener órdenes únicos
export const getUniqueOrdenes = (): string[] => {
  return Array.from(new Set(polygons.map((p) => p.orden)));
};
